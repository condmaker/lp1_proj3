namespace Roguelike
{
    /// <summary>
    /// A enumerate defining direction movement in the game 'board'.
    /// </summary>
    public enum Direction
    {
        /// <summary>
        /// Does not move.
        /// </summary>
        None,
        /// <summary>
        /// Moves upwards.
        /// </summary>
        Up,
        /// <summary>
        /// Moves rightwards.
        /// </summary>
        Right,
        /// <summary>
        /// Moves downwards.
        /// </summary>
        Down,
        /// <summary>
        /// Moves leftwards.
        /// </summary>
        Left
    }
}